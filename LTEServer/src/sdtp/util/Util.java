package sdtp.util;

public class Util {

	public static boolean checkNotNull(final Object value) {
		if (value == null) {
			return false;
		}
		return (value.toString().length() > 0);
	}

	public static boolean checkNull(final Object value) {
		if (value == null) {
			return true;
		}

		return (value.toString().length() == 0);

	}
}
